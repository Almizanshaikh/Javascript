// Arithmetic Operators

let a = 5;
let b = 3;

// addition
console.log("a + b = ", a + b); //8

// subtraction
console.log("a - b = ", a - b); //2

// multiplication 
console.log("a * b = ", a * b); //15

// division
console.log("a / b = ", a / b); //1.6666666666666667

// remainder
console.log("a % b = ", a % b); //2
  
//exponentiation
console.log("a**b = ", a ** b); //125

// increment  

console.log("++a = ", ++a); // x is now 6
console.log("a++ = ", a++); // prints 6 and then increased to 7
console.log("a = ", a); // 7

// decrement
console.log("--a = ", --a); // x is now 6
console.log("a-- = ", a--); // prints 6 and then decreased to 5
console.log("a = ", a); // 5

