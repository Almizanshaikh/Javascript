// js variable is a container for storing data
// in js we use the var or let keywords to declare varaible

// declare a variable

// var age;
// let name;

let firstname = "almizan";   // ( = ) assignment operator
console.log(firstname);

// change the value of variable

firstname = "shaikh";
console.log(firstname);

// here,the value of the variable is changed from almizan to shaikh when we assign a new value to it

// variable names must start with a letter an underscore

// Naming js variable
// valid 

// fir$tname
// $firstname
// _firstname
// first_name 
// ex  

let fir$tname = ("shaikh");
console.log(fir$tname);

// js constants
// A const is a type of variable whose value cannot be changed
// in js we use the const keyword to create constants 
// example

const pi = 3.14;
console.log(pi);

